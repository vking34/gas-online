$(document).ready(function(){
    $("#all").addClass("btnactive");
    $("#gas").click(function(){
        $("#gas").addClass("btnactive");
        $("#device").removeClass("btnactive");
        $("#service").removeClass("btnactive");
        $("#all").removeClass("btnactive");
        $(".gas").show(500);
        $(".device").hide(500);
        $(".service").hide(500);
    });
    $("#device").click(function(){
        $("#device").addClass("btnactive");
        $("#gas").removeClass("btnactive");
        $("#service").removeClass("btnactive");
        $("#all").removeClass("btnactive");
        $(".device").show(500);
        $(".gas").hide(500);
        $(".service").hide(500);
    });
    $("#service").click(function(){
        $("#service").addClass("btnactive");
        $("#device").removeClass("btnactive");
        $("#gas").removeClass("btnactive");
        $("#all").removeClass("btnactive");
        $(".service").show(500);
        $(".device").hide(500);
        $(".gas").hide(500);
    });
    $("#all").click(function(){
        $("#service").removeClass("btnactive");
        $("#device").removeClass("btnactive");
        $("#gas").removeClass("btnactive");
        $("#all").addClass("btnactive");
        $(".service").show(500);
        $(".device").show(500);
        $(".gas").show(500);
    });
});